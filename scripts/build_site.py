# ---------- helpers ----------------------------------------------------------


def _esc(s: object) -> str:
    return html.escape(str(s))


def _fmt(n: int) -> str:
    return f"{n:,}"


def page(title: str, here: str, body: str, *, byline: str = "") -> str:
    nav_html = " · ".join(
        f'<a class="{"active" if href == here else ""}" '
        f'href="{href}">{_esc(label)}</a>'
        for href, label in NAV
    )
    bline = (f'<p class="byline">{byline}</p>'
             if byline else "")
    return f"""<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>{_esc(title)} — ElfZoo corpus</title>
<link rel="stylesheet" href="style.css">
{CHART_CDN}
</head>
<body>
<header>
  <h1><a href="index.html">ElfZoo</a> — {_esc(title)}</h1>
  <nav>{nav_html}</nav>
  {bline}
</header>
<main>
{body}
</main>
<footer>
  <p>Generated by <code>scripts/build_site.py</code> from
     <code>corpus/survey.json</code>,
     <code>corpus/elflint_summary.json</code>, and
     <code>corpus/dep_graph.json</code>.
     Raw data: <a href="data.json">data.json</a>.</p>
</footer>
</body>
</html>
"""


def section(title: str, body: str, *, anchor: str | None = None) -> str:
    a = f' id="{_esc(anchor)}"' if anchor else ""
    return f'<section{a}><h2>{_esc(title)}</h2>\n{body}\n</section>\n'


def kv_card(label: str, value: str, sub: str = "") -> str:
    s = f'<div class="sub">{_esc(sub)}</div>' if sub else ""
    return (
        f'<div class="card">'
        f'<div class="num">{_esc(value)}</div>'
        f'<div class="lbl">{_esc(label)}</div>'
        f'{s}'
        f'</div>'
    )


def card_grid(cards: list[str]) -> str:
    return '<div class="grid">' + "\n".join(cards) + "</div>"


def table(headers: list[str], rows: list[list[str]],
          *, sortable: bool = True, klass: str = "data") -> str:
    cls = klass + (" sortable" if sortable else "")
    h = "".join(f"<th>{_esc(x)}</th>" for x in headers)
    r = "\n".join(
        "<tr>" + "".join(f"<td>{c}</td>" for c in row) + "</tr>"
        for row in rows
    )
    return (
        f'<table class="{cls}"><thead><tr>{h}</tr></thead>'
        f'<tbody>{r}</tbody></table>'
    )


def kv_table(d: dict, *, key_label: str = "value",
             val_label: str = "count", limit: int | None = None,
             total: int | None = None) -> str:
    items = sorted(d.items(), key=lambda kv: -_n(kv[1]))
    if limit is not None:
        items = items[:limit]
    if total is None:
        total = sum(_n(v) for v in d.values())
    rows = []
    for k, v in items:
        n = _n(v)
        pct = (n / total * 100) if total else 0.0
        rows.append([_esc(k), _fmt(n), f"{pct:.2f}%"])
    return table([key_label, val_label, "share"], rows)


def _n(v) -> int:
    if isinstance(v, dict) and "count" in v:
        return int(v["count"])
    try:
        return int(v)
    except (TypeError, ValueError):
        return 0


def chart_bar(canvas_id: str, labels: list[str],
              data: list[int], title: str = "",
              *, horizontal: bool = False,
              height: int = 280) -> str:
    cfg = {
        "type": "bar",
        "data": {
            "labels": labels,
            "datasets": [{
                "label": title,
                "data": data,
                "backgroundColor": "#3b6ea5",
                "borderRadius": 2,
            }],
        },
        "options": {
            "indexAxis": "y" if horizontal else "x",
            "responsive": True,
            "maintainAspectRatio": False,
            "plugins": {
                "legend": {"display": False},
                "title": {"display": bool(title), "text": title},
            },
            "scales": {"x": {"ticks": {"autoSkip": True}}},
        },
    }
    return (
        f'<div class="chart" style="height:{height}px">'
        f'<canvas id="{canvas_id}"></canvas></div>'
        f'<script>new Chart('
        f'document.getElementById({json.dumps(canvas_id)}),'
        f'{json.dumps(cfg)});</script>'
    )


def chart_pie(canvas_id: str, labels: list[str], data: list[int],
              title: str = "", *, height: int = 280) -> str:
    cfg = {
        "type": "doughnut",
        "data": {
            "labels": labels,
            "datasets": [{
                "data": data,
                "backgroundColor": [
                    "#3b6ea5", "#e07b3a", "#5d9b6c", "#a85a8e",
                    "#7a7a7a", "#c9b237", "#5a8fb3", "#b35a5a",
                    "#7d6ea0", "#56a39a", "#a39a56", "#9a56a3",
                ],
            }],
        },
        "options": {
            "responsive": True,
            "maintainAspectRatio": False,
            "plugins": {
                "legend": {"position": "right"},
                "title": {"display": bool(title), "text": title},
            },
        },
    }
    return (
        f'<div class="chart" style="height:{height}px">'
        f'<canvas id="{canvas_id}"></canvas></div>'
        f'<script>new Chart('
        f'document.getElementById({json.dumps(canvas_id)}),'
        f'{json.dumps(cfg)});</script>'
    )


def top_n(d: dict, n: int = 20) -> tuple[list[str], list[int]]:
    items = sorted(d.items(), key=lambda kv: -_n(kv[1]))[:n]
    return [str(k) for k, _ in items], [_n(v) for _, v in items]


def sample_list(samples: list[str], *, max_n: int = 10) -> str:
    if not samples:
        return "<em>(none)</em>"
    items = "".join(
        f"<li><code>{_esc(s)}</code></li>"
        for s in samples[:max_n]
    )
    extra = (
        f"<li><em>… and {len(samples) - max_n:,} more</em></li>"
        if len(samples) > max_n else ""
    )
    return f"<ul class='paths'>{items}{extra}</ul>"


def details(summary: str, body: str) -> str:
    return (
        f'<details><summary>{_esc(summary)}</summary>{body}</details>'
    )


