def main() -> int:
    if not SURVEY.is_file():
        print(f"build_site: {SURVEY} not found", file=sys.stderr)
        return 2
    if not ELFLINT.is_file():
        print(f"build_site: {ELFLINT} not found", file=sys.stderr)
        return 2

    survey = json.loads(SURVEY.read_text())
    el = json.loads(ELFLINT.read_text())
    g: dict | None = None
    if DEPGRAPH.is_file():
        g = json.loads(DEPGRAPH.read_text())
    else:
        print(f"build_site: {DEPGRAPH} not found — "
              "depgraph page will be skipped", file=sys.stderr)
    # Most pages drill into the loadable bucket — the actual
    # loader-spec target. Anomalies/categories pages keep access to
    # `all` and `by_category` for cross-bucket summaries.
    sv = survey["by_category"]["loadable"]
    sv_all = survey["all"]
    by_cat = survey["by_category"]
    cats = survey["category_counts"]

    SITE.mkdir(exist_ok=True)
    (SITE / "style.css").write_text(STYLE_CSS)
    # The dep_graph.json holds large path-keyed dicts (closures,
    # consumers, verneed, …) that are useless in the bundled
    # `data.json`. Embed only the slim summary keys.
    g_slim = None
    if g is not None:
        slim_keys = (
            "n_files", "n_providers", "n_consumers", "n_sonames",
            "n_conflicts", "n_external", "n_cycles",
            "n_version_mismatches", "closure_size_histogram",
            "top_in_degree", "top_out_degree", "biggest_closure",
            "conflicts", "external", "cycles", "version_mismatches",
            "soname_use",
        )
        g_slim = {k: g[k] for k in slim_keys if k in g}
    (SITE / "data.json").write_text(json.dumps(
        {"survey": survey, "elflint": el,
         **({"dep_graph": g_slim} if g_slim is not None else {})},
        indent=2) + "\n")

    n_total = survey["n_files"]
    n_loadable = sv["n_files"]
    byline_global = (
        f'Alpine main + community · x86_64 · {_fmt(n_total)} ELFs · '
        f'{_fmt(n_loadable)} loadable · static survey + '
        f'eu-elflint 0.190 oracle'
    )
    byline_loadable = (
        f'Loadable bucket — {_fmt(n_loadable)} ELFs '
        f'({n_loadable/n_total*100:.1f}% of {_fmt(n_total)} total). '
        f'See <a href="categories.html">Categories</a> for other '
        f'buckets.'
    )

    pages = [
        ("index.html",       "Dashboard",
         build_dashboard(survey, el, sv), byline_global),
        ("categories.html",  "Categories",
         build_categories(survey), byline_global),
        ("header.html",      "ELF Header",
         build_header(sv), byline_loadable),
        ("program.html",     "Program Headers",
         build_program(sv), byline_loadable),
        ("dynamic.html",     "Dynamic",
         build_dynamic(sv), byline_loadable),
        ("relocations.html", "Relocations",
         build_relocations(sv), byline_loadable),
        ("symbols.html",     "Symbols",
         build_symbols(sv), byline_loadable),
        ("notes.html",       "Notes & GNU props",
         build_notes(sv), byline_loadable),
        ("hardening.html",   "Hardening & ABI",
         build_hardening(sv), byline_loadable),
        ("packages.html",    "Packages",
         build_packages(sv), byline_loadable),
        ("needed.html",      "NEEDED graph",
         build_needed(sv), byline_loadable),
    ]
    if g is not None:
        pages.append(
            ("depgraph.html", "Dep graph",
             build_depgraph(g, sv), byline_loadable)
        )
    pages.extend([
        ("anomalies.html",   "Survey anomalies",
         build_anomalies(sv_all, by_cat), byline_global),
        ("elflint.html",     "elflint findings",
         build_elflint(el), byline_global),
    ])
    for href, title, body, byline in pages:
        (SITE / href).write_text(page(title, href, body, byline=byline))
        print(f"wrote {SITE / href}", file=sys.stderr)

    print(f"site built in {SITE}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
