def build_packages(sv: dict) -> str:
    pkg_files = sv["pkg_n_files"]
    pkg_relocs = sv["pkg_total_relocs"]
    top_files = sorted(pkg_files.items(), key=lambda kv: -_n(kv[1]))[:100]
    top_relocs = sorted(pkg_relocs.items(), key=lambda kv: -_n(kv[1]))[:100]
    return (
        section("Most ELF-heavy packages (top 100)",
                table(["ELF files", "package"],
                      [[_fmt(_n(v)), _esc(k)] for k, v in top_files]))
        + section("Most reloc-heavy packages (top 100)",
                  table(["relocations", "package"],
                        [[_fmt(_n(v)), _esc(k)] for k, v in top_relocs]))
        + section("Per-package summary", f"""
<p>The full corpus is split across <strong>{_fmt(len(pkg_files))}</strong>
packages. Median ELF files per package: <strong>{_median(pkg_files):.0f}</strong>;
99th percentile: <strong>{_pct(pkg_files, 99):.0f}</strong>.</p>
""")
    )


def build_needed(sv: dict) -> str:
    needed = sv["needed_libs"]
    hist = {str(k): v for k, v in sv["needed_count_histogram"].items()}
    return (
        section("Most-referenced DT_NEEDED libraries (top 50)",
                chart_bar("n_top", *top_n(needed, 25),
                          title="Top 25 NEEDED libs",
                          horizontal=True, height=520)
                + kv_table(needed, key_label="library", limit=50))
        + section("DT_NEEDED count per binary",
                  chart_bar("n_hist",
                            sorted(hist.keys(), key=lambda x: int(x)),
                            [_n(hist[k]) for k in sorted(hist.keys(),
                                                         key=lambda x: int(x))],
                            title="ELFs with N NEEDED entries"))
        + section("Most-NEEDED-heavy binaries (top 100)",
                  table(["NEEDED count", "file"],
                        [[_fmt(x["count"]), f"<code>{_esc(x['path'])}</code>"]
                         for x in sv["top_needed"][:100]]))
        + section("All distinct DT_NEEDED libraries", details(
            f"show all {_fmt(len(needed))} entries",
            kv_table(needed, key_label="library")
        ))
    )


