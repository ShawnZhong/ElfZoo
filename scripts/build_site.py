def build_relocations(sv: dict) -> str:
    n = sv["n_files"]
    parts = [
        section("Reloc type prevalence (top 50 of "
                f"{len(sv['reloc_types'])})",
                kv_table(sv["reloc_types"], key_label="reloc type",
                         limit=50)),
        section("Reloc categories", card_grid([
            kv_card("IFUNC reloc users",
                    _fmt(sv["files_with_ifunc_reloc"])),
            kv_card("COPY reloc users",
                    _fmt(sv["files_with_copy_reloc"]),
                    f'{sv["files_with_copy_reloc"]/n*100:.1f}%'),
            kv_card("TLS reloc users",
                    _fmt(sv["files_with_tls_reloc"]),
                    f'{sv["files_with_tls_reloc"]/n*100:.1f}%'),
            kv_card("no relocations",
                    _fmt(sv["files_no_relocations"]),
                    f'{sv["files_no_relocations"]/n*100:.1f}% of files'),
        ])),
    ]
    for arch, types in sorted(sv["reloc_types_per_arch"].items(),
                              key=lambda kv: -sum(_n(v) for v in kv[1].values())):
        if not types:
            continue
        parts.append(section(
            f"{arch}: reloc type distribution "
            f"({len(types)} types)",
            kv_table(types, key_label="reloc type", limit=40),
            anchor=f"arch-{arch}",
        ))
    parts.append(section(
        "Top relocations-per-binary (top 100)",
        table(["relocations", "file"],
              [[_fmt(x["count"]), f"<code>{_esc(x['path'])}</code>"]
               for x in sv["top_reloc"][:100]])
    ))
    return "".join(parts)


