def build_dashboard(survey: dict, el: dict, sv: dict) -> str:
    """The dashboard reports the **whole corpus** totals (sv["all"])
    plus a categories breakdown. Other pages drill into the loadable
    bucket, since that's the actual loader-spec target."""
    n = survey["n_files"]
    machines = sv["machine"]
    classes = sv["class"]
    cats = survey["category_counts"]
    loadable_n = survey["by_category"]["loadable"]["n_files"]
    cards = [
        kv_card("ELFs", _fmt(n), "across main + community"),
        kv_card("loadable", _fmt(loadable_n),
                f"{loadable_n/n*100:.1f}% — loader-spec target"),
        kv_card("packages", _fmt(len(sv["pkg_n_files"])),
                "unique <pkg-ver-rel> dirs"),
        kv_card("clean (elflint)", _fmt(el["n_clean"]),
                f'{el["n_clean"]/el["n_files"]*100:.1f}% of corpus'),
        kv_card("dirty (elflint)", _fmt(el["n_dirty"]),
                f'{el["n_dirty"]/el["n_files"]*100:.1f}% of corpus'),
        kv_card("elflint findings", _fmt(el["n_total_findings"]),
                f'{el["n_distinct_kinds"]} distinct kinds'),
        kv_card("survey anomalies", _fmt(sum(sv["anomalies"].values())),
                f'{len(sv["anomalies"])} kinds (all files)'),
        kv_card("relocation types", _fmt(len(sv["reloc_types"])),
                f'{len(sv["reloc_types_per_arch"])} architectures'),
        kv_card("NEEDED libraries", _fmt(len(sv["needed_libs"])),
                "distinct DT_NEEDED targets"),
        kv_card("PT_INTERP variants", _fmt(len(sv["interps"])),
                "distinct loader paths"),
        kv_card("BIND_NOW", _fmt(sv["files_with_bind_now"]),
                f'{sv["files_with_bind_now"]/max(1,sv["n_files"])*100:.1f}% '
                f'of all-files are full-RELRO'),
    ]

    clabels, cvals = zip(*sorted(cats.items(), key=lambda kv: -kv[1])) \
        if cats else ([], [])
    et = sv["elf_type"]
    etl, etv = zip(*sorted(et.items(), key=lambda kv: -kv[1])) \
        if et else ([], [])
    return (
        section("Corpus at a glance", card_grid(cards))
        + section("Categories",
                  chart_pie("ch_cats", list(clabels), list(cvals),
                            "ELFs per category")
                  + table(["category", "ELFs", "share"],
                          [[_esc(k), _fmt(v), f"{v/n*100:.2f}%"]
                           for k, v in sorted(cats.items(),
                                              key=lambda kv: -kv[1])])
                  + "<p>See the <a href='categories.html'>Categories</a> "
                  "page for full per-bucket detail. All other pages "
                  "show data from the <strong>loadable</strong> "
                  "bucket only — the real loader-spec target.</p>")
        + section("Loadable e_type",
                  "<p>The loadable bucket is by construction "
                  "<code>EM_X86_64</code> + <code>ELFCLASS64</code> + "
                  "<code>ELFDATA2LSB</code>. The only remaining "
                  "ELF-header axis with variety here is the file "
                  "type: shared objects vs executables. The "
                  "<a href='header.html'>ELF Header</a> page shows "
                  "the full corpus's variety on every axis.</p>"
                  + chart_pie("ch_et", list(etl), list(etv),
                              "ET_ distribution (loadable)"))
        + section("How to read this site", """
<p>This corpus is every <code>.apk</code> in Alpine
<strong>main + community / x86_64</strong>, unpacked and indexed by
<code>llvm-readobj-20 --elf-output-style=JSON</code>. Two parallel
static-analysis passes feed the site:</p>
<ul>
<li><strong>Survey</strong> (<code>scripts/survey.py</code>) — fast
    in-Python parser of the JSON dumps. Counts every ELF field,
    cross-references against the spec, classifies each file into
    one of five categories (loadable / debuginfo / relocatable /
    cross_arch / other) and emits structured anomalies (e.g.
    <code>ph_load_wx</code> when a PT_LOAD is W+X).</li>
<li><strong>eu-elflint oracle</strong>
    (<code>scripts/elflint_run.py</code>) — wraps elfutils 0.190
    <code>eu-elflint -q</code> over the same corpus, normalises each
    finding into a message template, and buckets by template.
    Independent second opinion: we agree on what's broken when both
    fire.</li>
</ul>
<p>Use the nav to drill into any axis. The
<a href="anomalies.html">anomalies</a> and
<a href="elflint.html">elflint</a> pages are the two strongest
signals about non-standard ELFs in this corpus.</p>
""")
    )


def build_header(sv: dict) -> str:
    machines = sv["machine"]
    mlabels, mvals = top_n(machines, 25)
    return (
        section("Machine (e_machine) — full distribution",
                chart_bar("h_mach_full", mlabels, mvals,
                          "ELFs per e_machine (top 25)",
                          height=420)
                + kv_table(machines, key_label="e_machine"))
        + section("ELF class (e_ident[EI_CLASS])",
                  chart_pie("h_cls", *top_n(sv["class"]))
                  + kv_table(sv["class"], key_label="ELFCLASS"))
        + section("Data encoding (e_ident[EI_DATA])",
                  chart_pie("h_data", *top_n(sv["data_encoding"]))
                  + kv_table(sv["data_encoding"], key_label="ELFDATA"))
        + section("OS/ABI (e_ident[EI_OSABI])",
                  chart_pie("h_osabi", *top_n(sv["osabi"]))
                  + kv_table(sv["osabi"], key_label="ELFOSABI"))
        + section("File type (e_type)",
                  chart_pie("h_etype", *top_n(sv["elf_type"]))
                  + kv_table(sv["elf_type"], key_label="ET_"))
        + section("Loader (PT_INTERP)",
                  kv_table(sv["interps"], key_label="PT_INTERP"))
    )


def build_program(sv: dict) -> str:
    ph = sv["phdr_types"]
    perms = sv["phdr_perm_combos"]
    seg = {str(k): v for k, v in sv["segment_count_histogram"].items()}
    ptload = {str(k): v for k, v in sv["pt_load_count_histogram"].items()}
    return (
        section("Program header types (PT_*)",
                chart_bar("p_types", *top_n(ph, 15),
                          title="ELFs containing each PT_*",
                          height=320)
                + kv_table(ph, key_label="PT_type"))
        + section("PT_LOAD permission combinations",
                  chart_pie("p_perms", *top_n(perms))
                  + kv_table(perms, key_label="permissions"))
        + section("PT_LOAD count distribution",
                  chart_bar("p_loadhist",
                            list(ptload.keys()),
                            [_n(v) for v in ptload.values()],
                            title="ELFs with N PT_LOAD segments"))
        + section("Total segment count distribution",
                  chart_bar("p_seghist",
                            list(seg.keys()),
                            [_n(v) for v in seg.values()],
                            title="ELFs with N total segments"))
        + section("Most-segmented binaries (top 100)",
                  table(["segments", "file"],
                        [[_fmt(x["count"]), f"<code>{_esc(x['path'])}</code>"]
                         for x in sv["top_segments"][:100]]))
    )


